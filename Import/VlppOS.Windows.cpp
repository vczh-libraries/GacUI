vint HttpClientApi::HexValue(wchar_t c)
{
	if (L'0' <= c && c <= L'9') return c - L'0';
	if (L'a' <= c && c <= L'f') return c - L'a' + 10;
	if (L'A' <= c && c <= L'F') return c - L'A' + 10;
	return -1;
}

